package com.java.basic;

import java.util.Scanner;

public class arithmeticOperations {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int m=sc.nextInt();
		int n=sc.nextInt();
		System.out.println(m+n);
		System.out.println(m-n);
		System.out.println(m*n);
		System.out.println(m/n);
		System.out.println(m%n);
	}
}
